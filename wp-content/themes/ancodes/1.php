        <h3 class="service-text-h3">Что же такое Linux?</h3>

        <div class="service-text-block">
            Нет такого человека, который бы не был знаком с операционной системой MS
            Windows. А вот о Linux некоторые могут слышать впервые.<br><br>Операционная система Linux используется в
            персональных компьютерах. Она значительно дешевле конкурентки, но выполняет такие же функции. Она может
            использоваться как частными лицами, так и организациями. Последним будет намного выгоднее использовать этот
            вид операционной системы. В первую очередь это связано с низкой ценой, что является существенным моментом
            для фирм.<br><br>Операционная система Linux современная, высокопроизводительная и достаточно надежная
            платформа для вашего бизнеса. Для установки ОС Linux вы можете пригласить специалиста, но если вы хоть
            чуть-чуть разбираетесь в установке ОС, то сможете сделать все самостоятельно.<br><br>Рассмотрим основные
            отличия MS Windows и Linux. Разработка и поддержка MS Windows закреплена только за работниками этой
            компании, в то время как поддержка Linux происходит от сотни компаний по всему миру. Использование Linux
            бесплатное, а за использование MS Windows придется платить (здесь имеется ввиду легально). MS Windows
            существует только с графической средой. В ОС Linux графическая среда отдельно. При использовании
            дистрибутива Linux, вы получаете систему, в которую не нужно ничего добавлять. При установке MS Windows вы
            получаете программу со скромной функциональностью, которая поможет вам установить необходимые прикладные
            программы.
